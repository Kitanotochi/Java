public class ConditionalOperator {
    //Операторы сравнения
    public static void main(String[] args) {
        int a = 7; //Создание переменной
        int b = 9; //Создание переменной
        if (a < b) {
            System.out.println("a < b"); //Вывод результата
        }
        else {
            System.out.println("a >= b");//Вывод результат
        }
    }
}